
exports.add = function(value_1, value_2){
    return value_1 + value_2;
}

exports.sub = function(value_1, value_2){
    return value_1 - value_2;
}

exports.mul = function(value_1, value_2){
    return value_1 * value_2;
}

exports.div = function(value_1, value_2){
    return value_1 / value_2;
}

